export const getAllDoctrines = () => {
  const allDoctrines = []
  Object.values(sdaDoctrines.categories).forEach(category => {
    category.doctrines.forEach(doctrine => {
      allDoctrines.push({
        ...doctrine,
        categoryName: category.name,
        categoryIcon: category.icon,
        categoryColor: category.color
      })
    })
  })
  return allDoctrines
}

export const getDoctrinesByCategory = (categoryKey) => {
  return sdaDoctrines.categories[categoryKey]?.doctrines || []
}

export const getDoctrineById = (id) => {
  return getAllDoctrines().find(doctrine => doctrine.id === id)
}

export const getDoctrineCounts = () => {
  const counts = {}
  Object.entries(sdaDoctrines.categories).forEach(([key, category]) => {
    counts[key] = category.doctrines.length
  })
  return counts
}

